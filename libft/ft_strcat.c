#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int cpt1;
	int cpt2;

	cpt1 = 0;
	cpt2 = 0;
	while (s1[cpt1] != '\0')
		cpt1++;
	while (s2[cpt2] != '\0')
	{
		s1[cpt1 + cpt2] = s2[cpt2];
		cpt2++;
	}
	s1[cpt1 + cpt2] = '\0';
	return (s1);
}
