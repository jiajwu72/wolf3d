#include "libft.h"

void			ft_free_sstr(char ***s)
{
	size_t	w;
	size_t	i;
	char	**t;

	t = *s;
	i = 0;
	w = ft_sstrlen(*s);
	while (i < w)
	{
		free(t[i]);
		i++;
	}
	free(t);
}
